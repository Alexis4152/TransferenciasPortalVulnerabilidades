package com.telcel.portal.pojos;

public class ConsultaPagosPojo {

	private String cuenta;
	private String respuesta;
	private String cuentaCambio;
	
	public String getCuenta() {
		return cuenta;
	}
	public void setCuenta(String cuenta) {
		this.cuenta = cuenta;
	}
	public String getCuentaCambio() {
		return cuentaCambio;
	}
	public void setCuentaCambio(String cuentaCambio) {
		this.cuentaCambio = cuentaCambio;
	}
	public String getRespuesta() {
		return respuesta;
	}
	public void setRespuesta(String respuesta) {
		this.respuesta = respuesta;
	}
	
	
}
